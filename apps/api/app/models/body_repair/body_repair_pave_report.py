"""BodyRepairPaveReport — parsed PAVE PDF metadata linked to a request.

A PAVE report is a vehicle condition snapshot generated by paveapi.com,
the system Amazon's DSP partners use to document fleet damage. Each row
here represents one PDF that was uploaded + parsed by
`services/pave_parser.py` (a near-verbatim port of NOVABODY/core's
parser at branch mbk/body-repair-demo).

A single `BodyRepairRequest` can have multiple PAVE reports:
  - `pre` phase — taken BEFORE the body repair work. The customer
    references this when submitting; the parser extracts the damage
    list to feed the parts/grade modes.
  - `post` phase — taken AFTER the work, used to compute the damage
    diff (resolved / still_present / new) at completion.

The PDF blob itself lives in MinIO (storage_path). This row stores
metadata + parsed fields + a JSON blob with the full structured
response (damages list, scores, etc.) for downstream UI.

Lifecycle: rows are created on `POST /body-repair/requests/{id}/pave`.
Parse runs synchronously inline — typical PAVE is ~2MB and parses in
under a second. On failure we still store the row with parse_status='failed'
so the SW can review manually.
"""

from datetime import datetime
from enum import Enum

import sqlalchemy as sa
from sqlalchemy import Column
from sqlmodel import Field, SQLModel

from app.models.base import timestamp_column, utc_now


class PavePhase(str, Enum):
    """Which side of the work this report represents."""

    PRE = "pre"          # snapshot before any repair
    POST = "post"        # snapshot after the repair completes
    UNASSIGNED = "unassigned"  # uploaded but not yet linked to a phase


class PaveParseStatus(str, Enum):
    """Mirrors the parser's `parse_status` field (str literals 'ok' /
    'failed') — kept as a typed enum on this side for index queries."""

    OK = "ok"
    FAILED = "failed"


class BodyRepairPaveReport(SQLModel, table=True):
    __tablename__ = "body_repair_pave_reports"

    id: int | None = Field(default=None, primary_key=True)

    # Owning body-repair request. CASCADE on delete so dropping a
    # request takes its attached PAVE rows with it; the PDF blobs in
    # MinIO get cleaned up by a separate sweeper job.
    request_id: int = Field(
        sa_column=Column(
            "request_id",
            sa.Integer,
            sa.ForeignKey("body_repair_requests.id", ondelete="CASCADE"),
            nullable=False,
            index=True,
        ),
    )

    phase: PavePhase = Field(
        default=PavePhase.PRE,
        sa_column=Column(
            "phase",
            sa.Enum(
                PavePhase,
                native_enum=False,
                length=15,
                values_callable=lambda e: [m.value for m in e],
            ),
            nullable=False,
            index=True,
        ),
    )

    # MinIO storage — the original PDF blob.
    storage_path: str = Field(max_length=500)
    file_size_bytes: int | None = Field(default=None)

    # Parsed fields surfaced as columns for fast filtering / search.
    # The full structured response (damages, scores per category, etc.)
    # lives in `parsed_json` below.
    parse_status: PaveParseStatus = Field(
        default=PaveParseStatus.OK,
        sa_column=Column(
            "parse_status",
            sa.Enum(
                PaveParseStatus,
                native_enum=False,
                length=10,
                values_callable=lambda e: [m.value for m in e],
            ),
            nullable=False,
            index=True,
        ),
    )
    vin: str | None = Field(default=None, max_length=20, index=True)
    year: int | None = Field(default=None)
    make: str | None = Field(default=None, max_length=50)
    model: str | None = Field(default=None, max_length=80)
    inspection_date_utc: datetime | None = Field(
        default=None,
        sa_column=Column("inspection_date_utc", sa.DateTime(timezone=True), nullable=True),
    )
    # PAVE returns a `scores` dict keyed by category (overall, body, etc.).
    # The most common single-number consumer wants is `total_score`.
    total_score: int | None = Field(default=None, index=True)
    damage_count: int = Field(default=0)

    # Full parsed payload — the dict shape returned by
    # `parse_pave_report()`. Keeps damage list, scores, side breakdowns,
    # parse_warnings, etc. close at hand without re-parsing the PDF.
    parsed_json: dict | None = Field(default=None, sa_column=Column("parsed_json", sa.JSON, nullable=True))

    # Source provenance — 'upload' (DSP uploaded), 'url' (paste of
    # reports.paveapi.com link), 'email' (Phase 5 inbox ingest).
    source: str | None = Field(default=None, max_length=20)
    source_url: str | None = Field(default=None, max_length=1000)

    uploaded_by_id: int | None = Field(
        default=None,
        sa_column=Column(
            "uploaded_by_id",
            sa.Integer,
            sa.ForeignKey("users.id", ondelete="SET NULL"),
            nullable=True,
        ),
    )

    created_at: datetime = Field(default_factory=utc_now, sa_column=timestamp_column("created_at"))
    updated_at: datetime = Field(default_factory=utc_now, sa_column=timestamp_column("updated_at"))

    @property
    def id_str(self) -> str:
        if self.id is None:
            return ""
        return f"BRP-{self.id:05d}"
